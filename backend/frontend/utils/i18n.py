# ── Pluralisation helper for Russian ───────────────────────────────────
# Simple form: {n} {...} — supports "1 thing|2 things|5 things" template
def _plural_ru(n: int, forms: str) -> str:
    """Russian plural: forms are 'один|два|пять'."""
    parts = forms.split("|")
    if n % 10 == 1 and n % 100 != 11:
        return parts[0]
    if 2 <= n % 10 <= 4 and (n % 100 < 10 or n % 100 >= 20):
        return parts[1] if len(parts) > 1 else parts[0]
    return parts[2] if len(parts) > 2 else (parts[1] if len(parts) > 1 else parts[0])


def _(key: str, **fmt: object) -> str:
    """Translate a key to the current language.

    Args:
        key: Dot-notation key into the translation dict (e.g. "dashboard.title").
        **fmt: Optional format arguments (e.g. n=42).

    Returns:
        Translated string with format arguments applied.
    """
    lang = st.session_state.get("language", "en")
    entry = T.get(key)
    if entry is None:
        return key  # fallback: return key as-is

    text = entry.get(lang, entry.get("en", key))
    if fmt:
        text = text.format(**fmt)
    return text
